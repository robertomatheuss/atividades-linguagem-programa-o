package Java.questao01;

public class Calculadora {
    public Double soma(Double primeiraParcela, Double segundaParcela){
        Double resultado = primeiraParcela+ segundaParcela;
        return resultado;
    }
    public Double subtrai(Double primeiraParcela, Double segundaParcela){
        Double resultado = primeiraParcela-segundaParcela;
        return resultado;
    }
    public Double multiplica(Double primeiraParcela, Double segundaParcela){
        Double resultado = primeiraParcela*segundaParcela;
        return resultado;
    }
    public Double divide(Double primeiraParcela, Double segundaParcela){
        Double resultado = primeiraParcela/segundaParcela;
        return resultado;
    }
}
